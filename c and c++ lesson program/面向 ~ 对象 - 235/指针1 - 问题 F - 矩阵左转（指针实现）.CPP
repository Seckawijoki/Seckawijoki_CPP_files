/*
题目描述
输入一个2*3的矩阵，将这个矩阵向左旋转90度后输出
比如现在有2*3矩阵 ：
1 2 3
4 5 6 

向左旋转90度后的矩阵变为：
3 6
2 5
1 4

要求：除了矩阵创建和数据输入可以使用数组和数组下标的方法，
其他过程对矩阵的任何访问都必须使用指针

输入
第一行输入t表示有t个测试实例
连续两行输入一个2*3的矩阵的数据
依次输入t个实例

输出
依次输出左转后的矩阵结果
在输出的每行中，每个数据之间都用空格隔开，
最后一个数据后面也带有空格

样例输入
2
1 2 3
4 5 6
4 5 6
7 8 9
样例输出
3 6 
2 5 
1 4 
6 9 
5 8 
4 7 
*/
# include <stdio.h>
int rotate( int n1[6] )
{
	int n2[3][3],i,j,*p;
	for ( i = 0 ; i < 2 ; i ++ )
	{
		for ( j = 0 ; j < 3 ; j ++ )
		{
			p = n1;
			n2[j][i] = *(p-1+(i+1)*3-j);//需想清楚。代数实例。
		}
	}
	for ( i = 0 ; i < 3  ; i ++ )
	{
		for ( j = 0 ; j < 1 ; j ++ )
			printf("%d ",n2[i][j]);
		printf("%d\n",n2[i][j]);
	}
	return 1;
}
int main()
{
	int t,n1[6],i;
	scanf("%d",&t);
	while ( t-- )
	{
		for ( i = 0 ; i < 6 ; i ++ )
				scanf("%d",&n1[i]);
		rotate(n1);
	}
	return 0;
}